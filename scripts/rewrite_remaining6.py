def get_pid(slug):
    r=requests.post(f'https://api.notion.com/v1/databases/{GDB}/query',headers=H,json={'filter':{'property':'slug','rich_text':{'contains':slug}},'page_size':5})
    return r.json()['results'][0]['id']

def wipe(pid):
    ids=[]
    cur=None
    while True:
        r=requests.get(f'https://api.notion.com/v1/blocks/{pid}/children',headers=H,params={'page_size':100,'start_cursor':cur} if cur else {'page_size':100})
        d=r.json()
        ids.extend(b['id'] for b in d.get('results',[]))
        if not d.get('has_more'):break
        cur=d.get('next_cursor')
    for b in ids:requests.delete(f'https://api.notion.com/v1/blocks/{b}',headers=H)
    return len(ids)

