for g in guides:
    # Check for duplicate slug first
    check = requests.post(f'https://api.notion.com/v1/databases/{GDB}/query', headers=H, json={
        'filter': {'property': 'slug', 'rich_text': {'equals': g['slug']}}, 'page_size': 3})
    results = check.json().get('results', [])
    if results:
        pid = results[0]['id']
        s = g['slug']; print(f'{s}: 이미 존재 (업데이트)')
        # Update existing
        requests.patch(f'https://api.notion.com/v1/pages/{pid}', headers=H, json={
            'properties': {
                'intro': {'rich_text': [{'text': {'content': g['intro']}}]},
                'title': {'title': [{'text': {'content': g['title']}}]}
            }})
        # Wipe and rewrite
        ids = []; cur = None
        while True:
            pp = {'page_size': 100}
            if cur: pp['start_cursor'] = cur
            r2 = requests.get(f'https://api.notion.com/v1/blocks/{pid}/children', headers=H, params=pp)
            d = r2.json()
            ids.extend(b['id'] for b in d.get('results', []))
            if not d.get('has_more'): break
            cur = d.get('next_cursor')
        for bid in ids: requests.delete(f'https://api.notion.com/v1/blocks/{bid}', headers=H)
        r3 = requests.patch(f'https://api.notion.com/v1/blocks/{pid}/children', headers=H, json={'children': g['blocks']})
        print(f'  {"OK" if r3.status_code==200 else f"FAIL {r3.status_code}"}')
    else:
        # Create new page
        print(f'{g["slug"]}: 신규 생성')
        body = {
            'parent': {'database_id': GDB},
            'properties': {
                'slug': {'rich_text': [{'text': {'content': g['slug']}}]},
                'title': {'title': [{'text': {'content': g['title']}}]},
                'intro': {'rich_text': [{'text': {'content': g['intro']}}]},
                'published': {'checkbox': True},
            },
            'children': g['blocks']
        }
        r = requests.post('https://api.notion.com/v1/pages', headers=H, json=body)
        if r.status_code == 200:
            print(f'  OK (new page)')
        else:
            print(f'  FAIL {r.status_code}: {r.text[:200]}')

